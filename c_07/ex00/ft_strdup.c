#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*duplicate;

	duplicate = (char *)malloc(ft_strlen(src) + 1);
	if (duplicate == NULL)
		return (NULL);
	ft_strcpy(duplicate, src);
	return (duplicate);
}
